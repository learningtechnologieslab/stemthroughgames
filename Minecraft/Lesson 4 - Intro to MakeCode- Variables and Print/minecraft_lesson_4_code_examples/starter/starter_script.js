// --- STEP 1: Declare your variables ---
// A variable is like a labeled chest in Minecraft.
// The label (name) never changes, but what's inside can.

let playerName = "Hero"
let health = 100
let score = 0

// --- STEP 2: Use player.say() to display each variable ---
// player.say() makes your Minecraft character speak in-game.
// This is how we "print" output in MakeCode.

player.say(playerName)
player.say("Health: " + health)
player.say("Score: " + score)

